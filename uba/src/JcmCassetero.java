
public class JcmCassetero {

	public Integer Denomincacion = 0;
	public int Disponibles = 0;
	public int Jcm = 0;
	public int Cassete = 0;
	public Integer getDenomincacion() {
		return Denomincacion;
	}
	public void setDenomincacion(Integer denomincacion) {
		Denomincacion = denomincacion;
	}
	public int getDisponibles() {
		return Disponibles;
	}
	public void setDisponibles(int disponibles) {
		Disponibles = disponibles;
	}
	public int getJcm() {
		return Jcm;
	}
	public void setJcm(int jcm) {
		Jcm = jcm;
	}
	public int getCassete() {
		return Cassete;
	}
	public void setCassete(int cassete) {
		Cassete = cassete;
	}
	
}
